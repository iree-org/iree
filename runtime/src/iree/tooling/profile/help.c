static void iree_profile_print_agent_markdown_commands(FILE* file) {
  fputs(
      "\n"
      "## Commands\n"
      "\n"
      "- `summary` checks bundle health, metadata counts, dispatch counts, "
      "clock\n"
      "  correlation, and per-device timing totals.\n"
      "- `statistics` reduces raw profile chunks through the same aggregate "
      "sink\n"
      "  used by lightweight runtime statistics. It emits compact row "
      "families\n"
      "  such as dispatch-by-export, queue operation, host execution, and "
      "memory\n"
      "  lifecycle aggregates without command-specific drilldown joins.\n"
      "- `att`, when built, decodes AMDGPU ATT/SQTT executable trace blobs "
      "with ROCm's\n"
      "  trace decoder and annotates decoded PCs using code objects embedded "
      "in\n"
      "  the `.ireeprof` bundle. Library lookup uses `--rocm_library_path` "
      "first,\n"
      "  then `IREE_HAL_AMDGPU_LIBAQLPROFILE_PATH`, then "
      "`IREE_HAL_AMDGPU_LIBHSA_PATH`,\n"
      "  then the normal system dynamic library search path.\n"
      "- `explain` gives an opinionated first-pass bottleneck view: visible "
      "device\n"
      "  spans, summed active dispatch time, merged per-queue busy intervals, "
      "top\n"
      "  exports, top individual dispatches, transfer totals, memory "
      "pressure,\n"
      "  and evidence-backed hints. Queue gap hints require queue event "
      "records;\n"
      "  do not infer queue dependency causes from dispatch record order.\n"
      "- `executable` lists executable/export metadata and joins export ids "
      "to\n"
      "  dispatch timing aggregates. Hash-capable producers include "
      "`code_object_hash`\n"
      "  on `executable` rows and `pipeline_hash` on `executable_export` "
      "rows for\n"
      "  cross-run and external-tool correlation.\n"
      "- `dispatch` groups timings by executable export, or emits every "
      "dispatch\n"
      "  event with `--dispatch_events`.\n"
      "- `command` lists command-buffer metadata, static operation records, "
      "and\n"
      "  execution spans grouped by `command_buffer_id` and `submission_id`.\n"
      "- `queue` groups dispatch-derived queue submissions by physical queue "
      "and\n"
      "  `submission_id`, and emits host queue-operation events when the "
      "producer\n"
      "  provides them. Queue events include operation type, wait/signal "
      "counts,\n"
      "  dependency strategy, payload bytes, and related object ids. "
      "Queue-device\n"
      "  events add raw device start/end ticks for timed submissions such as "
      "alloca,\n"
      "  execute, and dealloca.\n"
      "- `counter` joins hardware counter metadata and samples to "
      "dispatch/export,\n"
      "  command-buffer, queue, and submission ids. Aggregate mode emits "
      "per-counter\n"
      "  statistics; `--counter_samples --format=jsonl` emits every matched "
      "sample\n"
      "  row with raw values for drilldown.\n"
      "- `memory` summarizes slab/provider events, pool reservation events, "
      "async\n"
      "  queue alloca/dealloca, and synchronous HAL buffer allocation/free "
      "high-water behavior. Externally-owned imports are reported separately "
      "from IREE-owned allocations. With `--format=jsonl` it emits individual\n"
      "  `memory_event` rows before the summary and then emits `memory_pool` "
      "and\n"
      "  `memory_allocation` rows for pool/provider and lifecycle drilldown. "
      "It\n"
      "  separates reserved pool bytes, materialized pool bytes, queue "
      "in-flight\n"
      "  bytes, synchronous buffer bytes, and pool free/budget snapshots when "
      "the\n"
      "  producer supplies them. Open lifecycles at profile end or\n"
      "  partial closes can be normal capture-window/filter artifacts.\n"
      "- `export --format=ireeperf-jsonl` decodes `.ireeprof` into a "
      "schema-versioned\n"
      "  tooling interchange stream. This is the stable boundary for "
      "downstream\n"
      "  adapters, telemetry upload, database import, and optional renderers "
      "such as Perfetto or "
      "SQLite.\n"
      "- `cat` dumps raw bundle records and is mainly for format debugging.\n",
      file);
}

static void iree_profile_print_agent_markdown_jsonl_contracts(FILE* file) {
  fputs(
      "\n"
      "## JSONL Contracts\n"
      "\n"
      "Projection commands with `--format=jsonl` emit command-local report "
      "rows.\n"
      "Those rows are keyed by `type` and are optimized for interactive "
      "`jq`,\n"
      "agent analysis, and command-specific drilldown. The field names are "
      "kept\n"
      "boring and consistent across commands where the same concept appears, "
      "but\n"
      "these rows are still CLI reports, not the decoded bundle "
      "interchange\n"
      "schema.\n"
      "\n"
      "`export --format=ireeperf-jsonl` is the schema-versioned interchange "
      "path.\n"
      "Every row has `schema_version` and `record_type`, and the first row "
      "is a\n"
      "`schema` record describing time domains and clock-fit behavior. "
      "Use this\n"
      "stream for downstream adapters, telemetry upload, database imports, "
      "and\n"
      "renderers such as Perfetto/SQLite/HTML. Branch on `record_type` for "
      "export\n"
      "streams and on `type` for command report streams; do not merge the "
      "two\n"
      "families as if they had one row namespace.\n",
      file);
}

